# São operadores utilizados para atribuir um valor inicial ou
# sobrescrever o valor de uma variável.

# Atribuição do saldo com valor inicial igual a R$ 500
print('Adição         ......................................')
saldo = 500
print(saldo)

# saldo + 200
saldo += 200     
print(saldo)

print('Subtração      ......................................')
# Atribuição com Subtração e Multiplicação/Divisão
saldo = 500
saldo -= 100
print(saldo)

print('Multiplicação  ......................................')

saldo = 500
saldo *= 2
print(saldo)

print('Divisão        ......................................')
saldo = 500
saldo /= 5
print(saldo)

saldo = 500
saldo //= 5
print(saldo)

print('Módulo         ......................................')
saldo = 500
saldo %= 480
print(saldo)

print('Exponenciação  ......................................')
saldo = 80
saldo **= 2
print(saldo)